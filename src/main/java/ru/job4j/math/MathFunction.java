package ru.job4j.math;

public class MathFunction {
    public static double sum(double first, double second) {
        return first + second;
    }

    public static double multiply(double first, double second) {
        return first * second;
    }

    public static double aDifference(double first, double second) {
        return first - second;
    }

    public static double aDividing(double first, double second) {
        return first / second;
    }
}
